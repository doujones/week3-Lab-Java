    private void mniAddToOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mniAddToOrderActionPerformed
        // add selected burger to order

        // get the item price, quantity and calculate the order total
        double itemPrice = Double.parseDouble( txtItemPrice.getText() );
        int quantity = Integer.parseInt( txtQuantity.getText() );
        double order = itemPrice * quantity;

        // accumulate the order total to the entire total
        orderTotal += order;
        DecimalFormat fmt = new DecimalFormat("0.00");
        txtOrderTotal.setText( fmt.format(orderTotal) );

        // display the order on the txaOrder text area
        String output = txtQuantity.getText();
        output += " ";

        if( rdoSingle.isSelected() )
            output += "single";
        else
            output += "double";

        if(chkCheese.isSelected() )
            output += ", cheese";

        if(chkBacon.isSelected() )
            output += ", bacon";

        if(chkMeal.isSelected() )
            output += ", meal";

        output += " at ";

        output += txtItemPrice.getText();

        output += " each";

        output += "\n";     // remember to drop down to the next line

        txaOrder.append(output);

        // clear the inputs for the next order
        clearForNextOrder();
        
    }//GEN-LAST:event_mniAddToOrderActionPerformed

