    private void updatePrice() {
        // Add current burger price to order!
        double price = 0.0;
        
        if (rdoSingle.isSelected() )
            price = 3.50;
        else
            price = 4.75;
        
        // add on additional costs, if chosen
        if( chkCheese.isSelected())
            price += 0.50;
        if( chkBacon.isSelected())
            price += 1.25;
        if( chkMeal.isSelected())
            price += 4.00;
        
        DecimalFormat fmt = new DecimalFormat("0.00");
        txtItemPrice.setText( fmt.format(price));
    }

    private void clearForNextOrder() {
       // clear all items for next order
        btgBurgers.clearSelection();
        chkCheese.setSelected(false);
        chkBacon.setSelected(false);
        chkMeal.setSelected(false);
        txtItemPrice.setText("0");
        txtQuantity.setText("1");
    }

    private void resetEntireOrder() {
       orderTotal = 0.0;
       txtOrderTotal.setText("0");
       txaOrder.setText("");
       clearForNextOrder();
    }
